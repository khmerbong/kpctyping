def get_lesson(lesson_id):
    try:
        lid = int(lesson_id)
    except Exception:
        lid = 1
    for lesson in KPC_LESSONS:
        if lesson["id"] == lid:
            return lesson
    return KPC_LESSONS[0]


def lesson_groups():
    groups = []
    for cat in ["Beginner", "Intermediate", "Advanced"]:
        groups.append({"category": cat, "lessons": [l for l in KPC_LESSONS if l["category"] == cat]})
    return groups
