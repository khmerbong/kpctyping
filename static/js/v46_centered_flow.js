// V46 Centered Flow Typing Mode
// UI only. Does not alter backend, level data, XP/progress calculations, or routes.

(function(){
  var state = {
    baseText: "",
    index: 0,
    lastOriginal: "",
    combo: 0,
    lastCorrect: Number(localStorage.getItem("kpc_academy_correct") || 0)
  };

  function targetEl(){
    return document.getElementById("targetKey");
  }

  function normalizeText(txt){
    if(!txt) return "";
    return txt.replace(/\s+/g, " ").trim();
  }

  function escapeHtml(ch){
    if(ch === "&") return "&amp;";
    if(ch === "<") return "&lt;";
    if(ch === ">") return "&gt;";
    if(ch === '"') return "&quot;";
    if(ch === "'") return "&#39;";
    return ch;
  }

  function readRawTarget(){
    var el = targetEl();
    if(!el) return "";
    var raw = el.getAttribute("data-v46-text");
    if(raw) return raw;
    return normalizeText(el.textContent || "");
  }

  function seedFromCurrentTarget(){
    var el = targetEl();
    if(!el) return;

    var txt = normalizeText(el.textContent || "");
    if(!txt) return;

    // Strip text if it was generated by V46 already
    if(el.classList.contains("v46-active")){
      txt = state.baseText || txt;
    }

    if(txt !== state.lastOriginal && txt.length > 0){
      state.lastOriginal = txt;
      state.baseText = txt;
      state.index = 0;
      el.setAttribute("data-v46-text", txt);
    }
  }

  function currentWord(){
    var t = state.baseText || "";
    var i = state.index || 0;
    if(!t) return "";
    var left = t.lastIndexOf(" ", Math.max(0, i-1)) + 1;
    var right = t.indexOf(" ", i);
    if(right === -1) right = t.length;
    return t.slice(left, right);
  }

  function render(){
    var el = targetEl();
    if(!el) return;
    seedFromCurrentTarget();

    var text = state.baseText;
    if(!text) return;

    el.classList.add("v46-active");

    var current = Math.min(state.index, Math.max(0, text.length - 1));
    var start = Math.max(0, current - 5);
    var end = Math.min(text.length, current + 9);

    var html = '<div class="v46-flow-shell"><div class="v46-center-line"></div><div class="v46-flow-track">';
    if(start > 0) html += '<span class="v46-char done">…</span>';

    for(var i=start; i<end; i++){
      var ch = text[i] === " " ? "␣" : text[i];
      var cls = "v46-char ";
      if(i < current) cls += "done";
      else if(i === current) cls += "current";
      else cls += "future";

      // active word hint
      var left = text.lastIndexOf(" ", Math.max(0,current-1)) + 1;
      var right = text.indexOf(" ", current);
      if(right === -1) right = text.length;
      if(i >= left && i < right) cls += " word-active";

      html += '<span class="' + cls + '">' + escapeHtml(ch) + '</span>';
    }

    if(end < text.length) html += '<span class="v46-char future">…</span>';
    html += '</div><div class="v46-word-chip">Word: ' + escapeHtml(currentWord() || "Practice") + '</div></div>';
    el.innerHTML = html;
  }

  function xpPop(label, x, y){
    var p = document.createElement("div");
    p.className = "v46-xp-pop";
    p.textContent = label;
    p.style.left = (x || window.innerWidth / 2) + "px";
    p.style.top = (y || 230) + "px";
    document.body.appendChild(p);
    setTimeout(function(){ p.remove(); }, 850);
  }

  function comboGlow(){
    var c = document.querySelector(".v46-combo-glow");
    if(!c){
      c = document.createElement("div");
      c.className = "v46-combo-glow";
      document.body.appendChild(c);
    }
    c.textContent = "🔥 Combo x" + state.combo;
    if(state.combo >= 3){
      c.classList.add("show");
      clearTimeout(c.__timer);
      c.__timer = setTimeout(function(){ c.classList.remove("show"); }, 1300);
    }
  }

  function advanceOnKey(e){
    var text = state.baseText;
    if(!text) return;

    if(e.ctrlKey || e.metaKey || e.altKey) return;
    if(e.key.length !== 1 && e.key !== " ") return;

    var expected = text[state.index] || "";
    var typed = e.key === " " ? " " : e.key;

    if(expected && typed.toLowerCase() === expected.toLowerCase()){
      state.index = Math.min(state.index + 1, text.length - 1);
      state.combo += 1;
      xpPop("+XP", window.innerWidth / 2, 210);
      comboGlow();
      render();
    }else{
      state.combo = 0;
      comboGlow();
      xpPop("Miss", window.innerWidth / 2, 210);
    }
  }

  function monitorCoreLogic(){
    // If original logic changes target text/lesson, reseed.
    var el = targetEl();
    if(!el) return;

    var source = el.getAttribute("data-v46-text") || "";
    var coreTxt = normalizeText(el.textContent || "");
    if(!el.classList.contains("v46-active") && coreTxt && coreTxt !== source){
      seedFromCurrentTarget();
      render();
    }

    // Also react to correct count from existing logic.
    var now = Number(localStorage.getItem("kpc_academy_correct") || 0);
    if(now > state.lastCorrect){
      state.lastCorrect = now;
    }
  }

  function bind(){
    seedFromCurrentTarget();
    render();

    document.addEventListener("keydown", function(e){
      advanceOnKey(e);
      setTimeout(function(){
        monitorCoreLogic();
        render();
      }, 80);
    }, true);

    setInterval(function(){
      monitorCoreLogic();
      render();
    }, 900);
  }

  if(document.readyState === "loading"){
    document.addEventListener("DOMContentLoaded", bind);
  }else{
    bind();
  }
})();
